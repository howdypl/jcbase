package com.yanxin.iot.json2;

import java.util.Date;

public class YwbBean {

	private long ywbId= 0;
	private String ywbName = "";
	private String createTime;
	/**
	 * @return the ywbId
	 */
	public long getYwbId() {
		return ywbId;
	}
	/**
	 * @param ywbId the ywbId to set
	 */
	public void setYwbId(long ywbId) {
		this.ywbId = ywbId;
	}
	/**
	 * @return the ywbName
	 */
	public String getYwbName() {
		return ywbName;
	}
	/**
	 * @param ywbName the ywbName to set
	 */
	public void setYwbName(String ywbName) {
		this.ywbName = ywbName;
	}
	/**
	 * @return the createTime
	 */
	public String getCreateTime() {
		return createTime;
	}
	/**
	 * @param createTime the createTime to set
	 */
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	
	

}
